package com.xinxidu.xxd.utils.calendar;


public class CalenderBean {
    private String nunber;
    private String week;
    private boolean isCurrentItem;


    public String getNunber() {
        return nunber;
    }

    public void setNunber(String nunber) {
        this.nunber = nunber;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public boolean getCurrentItem() {
        return isCurrentItem;
    }

    public void setCurrentItem(boolean currentItem) {
        isCurrentItem = currentItem;
    }
}
